    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try{
        String consulta ="Select * from abonado where Id_Abonado="+this.txtAbonados.getText();
        str2=ObjBD.prepareStatement(consulta);
        resultado=str2.executeQuery(consulta);
        while(resultado.next()){
            this.lblAbonadoID.setText(resultado.getString("Id_Abonado"));
            this.lblClienteID.setText(resultado.getString("Id_Cliente"));
            if(resultado.getBoolean("Estado")==true){
                lblEstado.setText("La tarjeta esta siendo usada por otro vehiculo");
            }else{
                lblEstado.setText("Puede usar la Tarjeta");
                this.btnValidar.setEnabled(true);
            }
        }
        
        } catch (SQLException ex) {
        Logger.getLogger(JITicket.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnBuscarActionPerformed


