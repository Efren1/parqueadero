    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try{
        String consulta="select a.Estado,a.id_Abonado ,c.id_cliente,c.nombre_cliente,c.apellido_cliente,c.telefono_cliente,c.Direccion_cliente \n" +
                        "from abonado a , cliente c \n" +
                        "where a.id_abonado="+this.txtIdAbonado.getText() +" and c.id_cliente=a.id_cliente";
        str=ObjBD.prepareStatement(consulta);
        resultado=str.executeQuery(consulta);
        while(resultado.next()){
            if(resultado.getBoolean("a.Estado")==true)
                {
                JOptionPane.showMessageDialog(this,"Debe primero ser validado para la salida");
                this.dispose();
                }else{
                this.lblIdTarjeta.setText(resultado.getString("a.id_Abonado"));
                this.lblIdCliente.setText(resultado.getString("c.id_cliente"));
                this.lblNombre.setText(resultado.getString("c.nombre_cliente"));
                this.lblApellido.setText(resultado.getString("c.apellido_cliente"));
                this.lblTelefono.setText(resultado.getString("c.telefono_cliente"));
                this.lblDireccion.setText(resultado.getString("c.Direccion_cliente"));
                this.btnEliminar.setEnabled(true);
            }
        }
        }catch (SQLException ex) {
        Logger.getLogger(JITicket.class.getName()).log(Level.SEVERE, null, ex);

    }
    }//GEN-LAST:event_btnBuscarActionPerformed


